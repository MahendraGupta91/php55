              <h3>What is data?</h3>
              <p align="justify">Data are <i>facts</i> and <i>statistics</i> which are collected for analysis. 
                 Data can be anything text, image audio, video etc. Facts are truth values of physical property or phenomenon. 
                 Data are textual representation of those physical property or phenomenon. Data may not have some sense. If you collect some data from somewhere and show it to someone he or she may not be able
                 to understand about what this data is. Data are raw material kind of things. 
                 Someone may or may not be able to know whether the raw material will lead to which product or will not lead to any product.
              </p>
